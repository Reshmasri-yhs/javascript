//JavaScript Strings
//Create using Literals
// Using Single Quote
let s1 = 'abcd';
console.log(s1);

// Using Double Quote
let s2 = "abcd";
console.log(s2);

//Create using Constructor
let s = new String('abcd');
console.log(s);

//Template Literals (String Interpolation)
let s1 = 'gfg';
let s2 = `You are learning from ${s1}`;

console.log(s2);

//Empty String
let s1 = '';
let s2 = "";

console.log(s1);
console.log(s2);

//Multiline Strings (ES6 and later)
let s = `
    This is a
    multiline
    string`;

console.log(s);
